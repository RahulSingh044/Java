
public class literals {

    public static void main(String[] args) {
        // literals

        //binary format
        int num1 = 0b101;

        //hexadecimal format
        int num2 = 0x1A;

        //
        int num3 = 10_00_000;

        double num4 = 56;
        double num5 = 12e10;

        // the value of a will be incremented to b 
        char c = 'a';
        c++;

        System.out.println(c);
        System.out.println(num3);
    }
}
