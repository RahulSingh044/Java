class RelationalOperator {
    public static void main(String[] args) {
        int x = 9;
        int y = 10;

        boolean result = x < y;
        System.out.println("x < y: " + result);

        boolean result1 = x > y;
        System.out.println("x > y: " + result1);

        boolean result2 = x == y;
        System.out.println("x == y: " + result2);

        boolean result3 = x!= y;
        System.out.println("x!= y: " + result3);
    }
}