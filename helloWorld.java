class helloWorld{
    public static void main(String[] args) {
        // println is used to print the message in next line
        System.out.println("Hello, World!");
        // print is used to print the message in the same line
        System.out.print("Hello, World!");
        // \n is also used to print the message in the new line
        System.out.print("Hello, World!\n from rahul");
    }
}